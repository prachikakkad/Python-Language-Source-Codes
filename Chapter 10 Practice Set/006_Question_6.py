# Question :- 
"""Can you change the self parameter inside a class to something else (say "Prachi")? Try changing self to "slf" or "Prachi" and see the effects."""

# Answer :- Yes , we can change self to any other name because it is like the normal parameter of a function. It will work the same as the normal parameter of a function.

class check:
    def name(slf):
        print("My name is Prachi")

a = check()
a.name()